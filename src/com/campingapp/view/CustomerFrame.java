private void loadCustomers() {
    javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("Full Name");
    model.addColumn("Phone");
    model.addColumn("Email");

    try {
        com.campingapp.dao.CustomerDAO dao = new com.campingapp.dao.CustomerDAO();
        java.util.List<com.campingapp.model.Customer> customers = dao.getAllCustomers();
        for (com.campingapp.model.Customer c : customers) {
            model.addRow(new Object[]{c.getCustomerId(), c.getFullName(), c.getPhone(), c.getEmail()});
        }
    } catch (java.sql.SQLException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error loading customers: " + e.getMessage());
    }

    tblCustomers.setModel(model);
   }
