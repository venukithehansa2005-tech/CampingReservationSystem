    private void btnAddCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCustomerActionPerformed
        String fullName = txtFullName.getText().trim();
String phone = txtPhone.getText().trim();
String email = txtEmail.getText().trim();

if (fullName.isEmpty() || phone.isEmpty()) {
    javax.swing.JOptionPane.showMessageDialog(this, "Full Name and Phone are required.");
    return;
}

try {
    com.campingapp.model.Customer customer = new com.campingapp.model.Customer();
    customer.setFullName(fullName);
    customer.setPhone(phone);
    customer.setEmail(email);

    com.campingapp.dao.CustomerDAO dao = new com.campingapp.dao.CustomerDAO();
    dao.addCustomer(customer);

    javax.swing.JOptionPane.showMessageDialog(this, "Customer added successfully!");

    txtFullName.setText("");
    txtPhone.setText("");
    txtEmail.setText("");

    loadCustomers();

} catch (java.sql.SQLException e) {
    javax.swing.JOptionPane.showMessageDialog(this, "Error adding customer: " + e.getMessage());
}
    }//GEN-LAST:event_btnAddCustomerActionPerformed

