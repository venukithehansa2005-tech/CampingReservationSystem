    private void btnAddCampsiteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCampsiteActionPerformed
String siteName = txtSiteName.getText().trim();
String siteType = txtSiteType.getText().trim();
String priceText = txtPrice.getText().trim();
String capacityText = txtCapacity.getText().trim();

if (siteName.isEmpty() || siteType.isEmpty() || priceText.isEmpty() || capacityText.isEmpty()) {
    javax.swing.JOptionPane.showMessageDialog(this, "All fields are required.");
    return;
}

try {
    java.math.BigDecimal price = new java.math.BigDecimal(priceText);
    int capacity = Integer.parseInt(capacityText);

    com.campingapp.model.Campsite campsite = new com.campingapp.model.Campsite();
    campsite.setSiteName(siteName);
    campsite.setSiteType(siteType);
    campsite.setPricePerNight(price);
    campsite.setCapacity(capacity);
    campsite.setStatus("Available");

    com.campingapp.dao.CampsiteDAO dao = new com.campingapp.dao.CampsiteDAO();
    dao.addCampsite(campsite);

    javax.swing.JOptionPane.showMessageDialog(this, "Campsite added successfully!");

    txtSiteName.setText("");
    txtSiteType.setText("");
    txtPrice.setText("");
    txtCapacity.setText("");

    loadCampsites();

} catch (NumberFormatException e) {
    javax.swing.JOptionPane.showMessageDialog(this, "Price and Capacity must be valid numbers.");
} catch (java.sql.SQLException e) {
    javax.swing.JOptionPane.showMessageDialog(this, "Error adding campsite: " + e.getMessage());
}        
    }//GEN-LAST:event_btnAddCampsiteActionPerformed

