private void loadCampsites() {
    javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("Site Name");
    model.addColumn("Type");
    model.addColumn("Price/Night");
    model.addColumn("Capacity");
    model.addColumn("Status");

    try {
        com.campingapp.dao.CampsiteDAO dao = new com.campingapp.dao.CampsiteDAO();
        java.util.List<com.campingapp.model.Campsite> campsites = dao.getAllCampsites();
        for (com.campingapp.model.Campsite c : campsites) {
            model.addRow(new Object[]{
                c.getCampsiteId(), c.getSiteName(), c.getSiteType(),
                c.getPricePerNight(), c.getCapacity(), c.getStatus()
            });
        }
    } catch (java.sql.SQLException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error loading campsites: " + e.getMessage());
    }

    tblCampsites.setModel(model);
  }
