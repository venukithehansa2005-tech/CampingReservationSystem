private void loadCustomerDropdown() {
    try {
        com.campingapp.dao.CustomerDAO dao = new com.campingapp.dao.CustomerDAO();
        customerList = dao.getAllCustomers();
        cmbCustomer.removeAllItems();
        for (com.campingapp.model.Customer c : customerList) {
            cmbCustomer.addItem(c.getCustomerId() + " - " + c.getFullName());
        }
    } catch (java.sql.SQLException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error loading customers: " + e.getMessage());
    }
}

private void loadCampsiteDropdown() {
    try {
        com.campingapp.dao.CampsiteDAO dao = new com.campingapp.dao.CampsiteDAO();
        campsiteList = dao.getAllCampsites();
        cmbCampsite.removeAllItems();
        for (com.campingapp.model.Campsite c : campsiteList) {
            cmbCampsite.addItem(c.getCampsiteId() + " - " + c.getSiteName() + " ($" + c.getPricePerNight() + "/night)");
        }
    } catch (java.sql.SQLException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error loading campsites: " + e.getMessage());
    }
}

private void loadReservations() {
    javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("Customer ID");
    model.addColumn("Campsite ID");
    model.addColumn("Check-in");
    model.addColumn("Check-out");
    model.addColumn("Total Cost");
    model.addColumn("Status");

    try {
        com.campingapp.dao.ReservationDAO dao = new com.campingapp.dao.ReservationDAO();
        java.util.List<com.campingapp.model.Reservation> reservations = dao.getAllReservations();
        for (com.campingapp.model.Reservation r : reservations) {
            model.addRow(new Object[]{
                r.getReservationId(), r.getCustomerId(), r.getCampsiteId(),
                r.getCheckIn(), r.getCheckOut(), r.getTotalCost(), r.getStatus()
            });
        }
    } catch (java.sql.SQLException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error loading reservations: " + e.getMessage());
    }

    tblReservations.setModel(model);
  }
