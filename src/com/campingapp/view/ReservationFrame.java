    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
try {
    int customerIndex = cmbCustomer.getSelectedIndex();
    int campsiteIndex = cmbCampsite.getSelectedIndex();

    if (customerIndex == -1 || campsiteIndex == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a customer and campsite.");
        return;
    }

    com.campingapp.model.Customer selectedCustomer = customerList.get(customerIndex);
    com.campingapp.model.Campsite selectedCampsite = campsiteList.get(campsiteIndex);

    java.sql.Date checkIn = java.sql.Date.valueOf(txtCheckIn.getText().trim());
    java.sql.Date checkOut = java.sql.Date.valueOf(txtCheckOut.getText().trim());

    long nights = (checkOut.getTime() - checkIn.getTime()) / (1000 * 60 * 60 * 24);
    if (nights <= 0) {
        javax.swing.JOptionPane.showMessageDialog(this, "Check-out date must be after check-in date.");
        return;
    }

    java.math.BigDecimal totalCost = selectedCampsite.getPricePerNight().multiply(java.math.BigDecimal.valueOf(nights));

    com.campingapp.model.Reservation reservation = new com.campingapp.model.Reservation();
    reservation.setCustomerId(selectedCustomer.getCustomerId());
    reservation.setCampsiteId(selectedCampsite.getCampsiteId());
    reservation.setCheckIn(checkIn);
    reservation.setCheckOut(checkOut);
    reservation.setTotalCost(totalCost);
    reservation.setStatus("Booked");

    com.campingapp.dao.ReservationDAO dao = new com.campingapp.dao.ReservationDAO();
    dao.addReservation(reservation);

    javax.swing.JOptionPane.showMessageDialog(this, "Reservation confirmed! Total: " + totalCost);

    txtCheckIn.setText("");
    txtCheckOut.setText("");
    lblTotalCost.setText("0.00");
    loadReservations();

} catch (com.campingapp.exception.ReservationConflictException e) {
    javax.swing.JOptionPane.showMessageDialog(this, "Booking failed: " + e.getMessage());
} catch (IllegalArgumentException e) {
    javax.swing.JOptionPane.showMessageDialog(this, "Please enter valid dates in format yyyy-mm-dd.");
} catch (java.sql.SQLException e) {
    javax.swing.JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
}        
    }//GEN-LAST:event_btnConfirmActionPerformed

    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
try {
    int selectedIndex = cmbCampsite.getSelectedIndex();
    if (selectedIndex == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a campsite.");
        return;
    }

    com.campingapp.model.Campsite selectedCampsite = campsiteList.get(selectedIndex);

    java.sql.Date checkIn = java.sql.Date.valueOf(txtCheckIn.getText().trim());
    java.sql.Date checkOut = java.sql.Date.valueOf(txtCheckOut.getText().trim());

    long nights = (checkOut.getTime() - checkIn.getTime()) / (1000 * 60 * 60 * 24);

    if (nights <= 0) {
        javax.swing.JOptionPane.showMessageDialog(this, "Check-out date must be after check-in date.");
        return;
    }

    java.math.BigDecimal totalCost = selectedCampsite.getPricePerNight().multiply(java.math.BigDecimal.valueOf(nights));
    lblTotalCost.setText(totalCost.toString());

} catch (IllegalArgumentException e) {
    javax.swing.JOptionPane.showMessageDialog(this, "Please enter valid dates in format yyyy-mm-dd.");
}        
    }//GEN-LAST:event_btnCalculateActionPerformed

